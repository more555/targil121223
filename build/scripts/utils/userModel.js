export class User {
    constructor(userID, firstName, lastName, email, password, avatar, isSeller, company, donuts) {
        this.userID = userID;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.password = password;
        this.avatar = avatar;
        this.isSeller = isSeller;
        this.company = company;
        this.donuts = donuts;
    }
}
